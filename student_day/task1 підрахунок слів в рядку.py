# Функція для підрахунку слів в рядку
def count_words(text):
    # Розділіть рядок на слова, використовуючи пробіли як роздільник
    words = text.split()

    # Створіть пустий словник для зберігання результатів
    word_count = {}

    # Пройдіться по кожному слову і підрахуйте кількість входжень
    for word in words:
        # Перевірте, чи слово вже є в словнику
        if word in word_count:
            # Якщо так, збільште лічильник на 1
            word_count[word] += 1
        else:
            # Якщо слово ще не зустрічалося, додайте його в словник зі значенням 1
            word_count[word] = 1

    return word_count


# Введіть рядок тексту
text = input("Введіть рядок тексту: ")

# Викличемо функцію для підрахунку слів
result = count_words(text)

# Виведемо результат
for word, count in result.items():
    print(f"'{word}': {count} раз(ів)")

